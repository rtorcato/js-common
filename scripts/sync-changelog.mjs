// Copy the root CHANGELOG.md into apps/doc/docs/changelog.md with Docusaurus
// frontmatter, so semantic-release keeps owning a single canonical changelog
// while the docs site renders it in-nav. The output file is gitignored — it is
// regenerated on every docs build (wired as apps/doc's `prebuild` script).

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, '..')
const source = resolve(repoRoot, 'CHANGELOG.md')
const target = resolve(repoRoot, 'apps/doc/docs/changelog.md')

const body = readFileSync(source, 'utf8')

const frontmatter = `---
title: Changelog
description: Release notes for @rtorcato/js-common, generated by semantic-release.
---

`

writeFileSync(target, frontmatter + body)
console.log(`sync-changelog: wrote ${target}`)
